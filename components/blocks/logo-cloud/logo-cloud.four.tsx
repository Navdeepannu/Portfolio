function CornerDots({ index }: { index: number }) {
  const duration = randomBetween(index, 2.5, 4.8, 3)
  const delay = randomBetween(index, 0, 2.2, 4)

  const dots = [
    'left-0 top-0',
    'right-0 top-0',
    'bottom-0 left-0',
    'bottom-0 right-0',
  ]

  return (
    <>
      {dots.map((position, dotIndex) => (
        <motion.span
          key={position}
          aria-hidden="true"
          className={cn(
            'pointer-events-none absolute z-20 size-1 rounded-full bg-zinc-400/70 dark:bg-white/35',
            position.includes('left') && '-translate-x-1/2',
            position.includes('right') && 'translate-x-1/2',
            position.includes('top') && '-translate-y-1/2',
            position.includes('bottom') && 'translate-y-1/2',
            position,
          )}
          animate={{
            opacity: [0.15, 0.95, 0.25, 0.7, 0.15],
            scale: [1, 1.2, 0.9, 1.08, 1],
          }}
          transition={{
            duration,
            delay: delay + dotIndex * 0.2,
            repeat: Number.POSITIVE_INFINITY,
            ease: 'easeInOut',
          }}
        />
      ))}
    </>
  )
}