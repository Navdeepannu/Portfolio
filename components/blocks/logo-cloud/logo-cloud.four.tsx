const springConfig = {
  stiffness: 180,
  damping: 26,
  mass: 0.8,
}

function seededRandom(index: number, offset = 0) {
  const value = Math.sin((index + 1) * 999 + offset) * 10000
  return value - Math.floor(value)
}

function randomBetween(index: number, min: number, max: number, offset = 0) {
  return min + seededRandom(index, offset) * (max - min)
}

export default function LogoCloudFour() {
  const wrapperRef = useRef<HTMLDivElement>(null)

  const mouseX = useMotionValue(-1000)
  const mouseY = useMotionValue(-1000)

  const smoothX = useSpring(mouseX, springConfig)
  const smoothY = useSpring(mouseY, springConfig)

  const cursorGlow = useMotionTemplate`
    radial-gradient(
      420px circle at ${smoothX}px ${smoothY}px,
      rgb(163 230 53 / 0.18),
      rgb(163 230 53 / 0.08) 32%,
      transparent 68%
    )
  `

  function handlePointerMove(event: React.PointerEvent<HTMLDivElement>) {
    const rect = wrapperRef.current?.getBoundingClientRect()

    if (!rect) return

    mouseX.set(event.clientX - rect.left)
    mouseY.set(event.clientY - rect.top)
  }

  function handlePointerLeave() {
    mouseX.set(-1000)
    mouseY.set(-1000)
  }

  return (
    <section className="bg-background py-20">
      <div className="mx-auto w-full max-w-6xl px-6">
        <div
          ref={wrapperRef}
          onPointerMove={handlePointerMove}
          onPointerLeave={handlePointerLeave}
          className="group/logo-cloud relative isolate overflow-hidden rounded-3xl"
        >
          <motion.div
            aria-hidden="true"
            className="pointer-events-none absolute inset-0 z-0 opacity-0 transition-opacity duration-300 group-hover/logo-cloud:opacity-100"
            style={{ background: cursorGlow }}
          />

          <div
            role="list"
            className="relative z-10 grid grid-cols-2 sm:grid-cols-3 lg:grid-cols-4"
          >
            {companies.map((company, index) => (
              <LogoCell key={company.name} company={company} index={index} />
            ))}
          </div>
        </div>
      </div>
    </section>
  )
}

